    private void jcmb_fajtaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcmb_fajtaActionPerformed
        // TODO add your handling code here:
        int szamlalo = 0;
        
        for (KutyaClass kutya : Kutyak) {
            if (kutya.getNem().equals(jcmb_fajta.getSelectedItem().toString())) {
                szamlalo++;
            }
        }
        
        jtxt_kutyakSzama.setText(String.valueOf(szamlalo));
        
        
    }//GEN-LAST:event_jcmb_fajtaActionPerformed
    
    
    
