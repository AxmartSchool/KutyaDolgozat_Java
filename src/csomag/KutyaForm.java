    private void insertTable() {
        int sorokszama = model.getRowCount();
        for (int i = sorokszama; i > 0; i--) {
            model.removeRow(i);
            
        }
        
        for (KutyaClass kutya : Kutyak) {
            
            model.addRow(new Object[]{kutya.getNev(),kutya.getFajta()});
            
            
        }
        
        
    }
